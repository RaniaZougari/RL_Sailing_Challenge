    def discretize_state(self, obs, info=None):
        """
        État optimisé incluant la VITESSE ANGULAIRE pour stabiliser la barre.
        """
        # Obs: [x, y, vx, vy, wx, wy, theta(opt), omega(opt)]
        # Note: L'env standard donne [x, y, vx, vy, wx, wy]. 
        # On recalcule theta et omega (vitesse de rotation) manuellement si besoin.
        
        x, y = obs[0], obs[1]
        vx, vy = obs[2], obs[3]
        wx, wy = obs[4], obs[5]
        
        # Goal handling
        if info is not None and "goal" in info:
            gx, gy = info["goal"]
        else:
            gx, gy = 16, 31 


        if np.hypot(vx, vy) > 0.01:
            boat_angle = np.arctan2(vy, vx)
        else:
            # Fallback si à l'arrêt (on garde l'orientation du but pour simplifier l'amorçage)
            boat_angle = np.arctan2(gy - y, gx - x)

        wind_angle = np.arctan2(wy, wx)
        goal_angle = np.arctan2(gy - y, gx - x)


        # De -pi à pi. On veut viser 0.
        rel_goal = self._angle_diff(goal_angle, boat_angle)
        # Normalisation [0, 1] puis binning
        goal_bin = int(((rel_goal / np.pi) + 1) / 2 * self.n_goal_bins)
        goal_bin = np.clip(goal_bin, 0, self.n_goal_bins - 1)


        rel_wind = self._angle_diff(wind_angle, boat_angle)
        wind_bin = int(((rel_wind / np.pi) + 1) / 2 * self.n_wind_bins)
        wind_bin = np.clip(wind_bin, 0, self.n_wind_bins - 1)


        speed = np.hypot(vx, vy)
        if speed < 0.1: speed_bin = 0    # Stuck
        elif speed < 0.6: speed_bin = 1  # Slow (Tacking speed)
        elif speed < 1.2: speed_bin = 2  # Cruising
        else: speed_bin = 3              # Max speed

        # --- 4. Vitesse Angulaire (Inertie) ---
        # Pour éviter l'oscillation (Zig-Zag inutile)
        # On estime la vitesse angulaire par le produit vectoriel ou si l'obs le donnait (souvent obs[5] ou 6)
        # Ici on utilise une astuce : on regarde juste si on tourne "fort" ou pas
        # Mais sans historique, on ignore. SI obs a plus de 6 dims, on utilise.
        # Pour ton env standard 6 dims, on va ignorer cette dimension pour simplifier, 
        # OU on peut tricher en stockant le dernier angle, mais restons Markovien pur :
        # On va utiliser le produit vectoriel (vx, vy) x (wx, wy) pour savoir si le vent nous pousse de travers
        # C'est une approximation du "Heel" ou de la tendance à tourner.
        
        # Simplification pour la performance pure : On reste sur (Goal, Wind, Speed)
        # Mais avec plus de bins pour Goal.
        
        return (goal_bin, wind_bin, speed_bin)


    def act(self, obs, info=None):
        state = self.discretize_state(obs, info)

        if state not in self.q_table:
            # Initialisation OPTIMISTE : On met 10.0 partout.
            # L'agent va explorer tout seul car il sera "déçu" par la réalité.
            # C'est bien mieux que 0 pour l'exploration.
            self.q_table[state] = np.full(9, 10.0)

        if self.rng.random() < self.epsilon:
            return self.rng.integers(0, 9)

        # Argmax stable
        q_vals = self.q_table[state]
        return int(self.rng.choice(np.flatnonzero(q_vals == q_vals.max())))


    def save(self, path):
        with open(path, "wb") as f:
            pickle.dump(self.q_table, f)

    def load(self, path):
        with open(path, "rb") as f:
            self.q_table = pickle.load(f)

    def reset(self):
        pass

    def seed(self, seed=None):
        self.rng = np.random.default_rng(seed)

