    def _angle(self, v):
        return np.arctan2(v[1], v[0])

    def _angle_diff(self, a, b):
        d = a - b
        return (d + np.pi) % (2 * np.pi) - np.pi

    def _point_of_sail(self, boat_angle, wind_angle):
        rel = abs(self._angle_diff(boat_angle, wind_angle))

        if rel < self.no_go_angle:
            return 0  # No-go
        elif rel < np.pi / 3:
            return 1  # Close-hauled
        elif rel < np.pi / 2:
            return 2  # Close reach
        elif rel < 2 * np.pi / 3:
            return 3  # Beam reach
        elif rel < 5 * np.pi / 6:
            return 4  # Broad reach
        else:
            return 5  # Running

    # ============================================================
    # STATE DISCRETIZATION
    # ============================================================

    def discretize_state(self, obs, info=None):
        """
         FIXED: Gre le cas o info est None (pour soumission Codabench).
        """
        x, y = obs[0], obs[1]
        vx, vy = obs[2], obs[3]
        wx, wy = obs[4], obs[5]

        #  Goal: Utiliser info si disponible, sinon valeur par dfaut
        if info is not None and "goal" in info:
            gx, gy = info["goal"]
        else:
            # Valeur par dfaut pour Codabench (goal standard)
            gx, gy = 16, 31

        # Vectors
        boat_vel = np.array([vx, vy])
        wind = np.array([wx, wy])
        to_goal = np.array([gx - x, gy - y])

        speed = np.linalg.norm(boat_vel)
        distance = np.linalg.norm(to_goal)

        boat_angle = (
            np.arctan2(vy, vx) if speed > 0.05
            else np.arctan2(to_goal[1], to_goal[0])
        )
        wind_angle = np.arctan2(wy, wx)
        goal_angle = np.arctan2(to_goal[1], to_goal[0])

        # Point of sail
        rel = abs((boat_angle - wind_angle + np.pi) % (2*np.pi) - np.pi)

        if rel < np.pi/4:
            sail_bin = 0      # NO GO
        elif rel < np.pi/3:
            sail_bin = 1
        elif rel < np.pi/2:
            sail_bin = 2
        elif rel < 2*np.pi/3:
            sail_bin = 3      # BEAM
        elif rel < 5*np.pi/6:
            sail_bin = 4
        else:
            sail_bin = 5

        # Goal direction
        rel_goal = (goal_angle - boat_angle + np.pi) % (2*np.pi) - np.pi
        goal_dir_bin = int((rel_goal + np.pi) / (2*np.pi) * self.goal_dir_bins)
        goal_dir_bin = np.clip(goal_dir_bin, 0, self.goal_dir_bins - 1)

        # Distance bin
        if distance > 20:
            dist_bin = 0
        elif distance > 8:
            dist_bin = 1
        else:
            dist_bin = 2

        # Speed bin
        if speed < 0.3:
            speed_bin = 0
        elif speed < 1.0:
            speed_bin = 1
        else:
            speed_bin = 2

        return (sail_bin, goal_dir_bin, dist_bin, speed_bin)

    # ============================================================
    # ACTION SELECTION
    # ============================================================

    def act(self, obs, info=None):
        """ FIXED: info est optionnel."""
        state = self.discretize_state(obs, info)

        if state not in self.q_table:
            self.q_table[state] = np.zeros(9)

        # Epsilon-greedy
        if self.rng.random() < self.epsilon:
            return self.rng.integers(0, 9)

        return int(np.argmax(self.q_table[state]))

    # ============================================================
    # LEARNING
    # ============================================================

    def learn(self, obs, action, reward, next_obs, done, info=None):
        """ FIXED: info est optionnel."""
        state = self.discretize_state(obs, info)
        next_state = self.discretize_state(next_obs, info)

        if state not in self.q_table:
            self.q_table[state] = np.zeros(9)
        if next_state not in self.q_table:
            self.q_table[next_state] = np.zeros(9)

        # Terminal bonus
        if done:
            reward += 100.0

        best_next = np.max(self.q_table[next_state])
        td_target = reward + self.gamma * best_next
        td_error = td_target - self.q_table[state][action]

        self.q_table[state][action] += self.alpha * td_error

        # Decay exploration
        self.epsilon = max(self.epsilon * self.epsilon_decay, self.epsilon_min)

    # ============================================================
    # REWARD SHAPING
    # ============================================================

    def shaped_reward(self, obs, next_obs, info=None):
        """ FIXED: info est optionnel."""
        x, y = obs[0], obs[1]
        vx, vy = obs[2], obs[3]
        wx, wy = obs[4], obs[5]
        
        nx, ny = next_obs[0], next_obs[1]

        # Goal
        if info is not None and "goal" in info:
            gx, gy = info["goal"]
        else:
            gx, gy = 16, 31

        to_goal = np.array([gx - x, gy - y])
        to_goal_norm = np.linalg.norm(to_goal)
        if to_goal_norm < 1e-6:
            return 0.0

        direction = to_goal / to_goal_norm
        displacement = np.array([nx - x, ny - y])

        # Projection toward goal
        progress = np.dot(displacement, direction)

        # Wind physics bonus
        boat_angle = self._angle(displacement)
        wind_angle = self._angle(np.array([wx, wy]))
        sail_bin = self._point_of_sail(boat_angle, wind_angle)

        polar_bonus = {
            0: -10.0,  # NO-GO
            1: 0.5,
            2: 1.0,
            3: 2.0,   # BEAM REACH (best)
            4: 1.5,
            5: 0.8
        }

        return 5.0 * progress + polar_bonus.get(sail_bin, 0)

