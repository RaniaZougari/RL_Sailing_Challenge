    def act(self, observation, info=None):
        state = self.discretize_state(observation)
        return self.q_table.get(state, 8)  # action 8 = stay

    def discretize_state(self, obs):
        x, y, vx, vy, wx, wy = obs[:6]

        x_bin = min(7, max(0, int(x / 32 * 8)))
        y_bin = min(7, max(0, int(y / 32 * 8)))

        v_angle = np.arctan2(vy, vx)
        v_bin = int((v_angle + np.pi) / (2 * np.pi) * 8) % 8

        w_angle = np.arctan2(wy, wx)
        w_bin = int((w_angle + np.pi) / (2 * np.pi) * 8) % 8

        return (x_bin, y_bin, v_bin, w_bin)

